		ORG	300Q
SCRPG	EQU	11Q
OP1		EQU	0Q
OP2		EQU OP1 + 4
RSULT	EQU OP2 + 4
SCR 	EQU RSULT + 4
INPUT	EQU 0FF3H
CVRT	EQU 0FF0H
LDIV	EQU	0FE1H
;
		MVI H,SCRPG
		MVI L,OP1
		MVI C,SCR
		CALL INPUT
		MVI L,OP2
		MVI C,SCR
		CALL INPUT
		MVI L,OP1
		MVI B,OP2
		MVI C,RSULT
		CALL LDIV
;
		MVI L,RSULT
		MVI C,SCR
		CALL CVRT
;
